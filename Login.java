    private void login_btm2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_login_btm2MouseClicked
        String user = user_field.getText();
        String pass = String.valueOf(pass_field.getPassword());
        String query = "select * from personal where email = "+"'"+user+"'"+";";
        
        Encript code = new Encript();
            String hash_login = null;
        try {
            hash_login = code.Sha1(user, pass);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try{
        Cconexion conectar = new Cconexion();
        Connection conexion = conectar.EstablecerConexion();
        java.sql.Statement st = conexion.createStatement();
        ResultSet rs = st.executeQuery(query); //aqui ejecutamos el query
        
        if(rs.next()) {
            String hash_base = rs.getString("password");
            if (hash_base.equals(hash_login)) {
                //Se valido la entrada
                setVisible(false);
                String nombre_1 = rs.getString("nombre_1");       
                String nombre_2 = rs.getString("nombre_2");
                String apellidos = rs.getString("apellidos");
                String puesto = rs.getString("puesto");
                String email = rs.getString("email");
                String telefono = rs.getString("telefono");
                boolean activo = rs.getBoolean("activo");
                
                Menu menu = new Menu();
                menu.setUser(nombre_1, nombre_2, apellidos, puesto, email, telefono, activo);
                new Opciones().setVisible(true);
                
                //JOptionPane.showMessageDialog(null,"Usuario y Contrase単a correctos","happy",JOptionPane.INFORMATION_MESSAGE);
            } else {
                //NO Se valido una entrada no original
                JOptionPane.showMessageDialog(null,"No se encontro el usuario o contrase単a","Error",JOptionPane.ERROR_MESSAGE);
            }
        } else {
            //NO SE VALIDO LA ENTRADA ORIGINAL  
            JOptionPane.showMessageDialog(null,"No se ingreso usuario o contrase単a","Error",JOptionPane.ERROR_MESSAGE);
        }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e.toString(),"Error",JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_login_btm2MouseClicked

